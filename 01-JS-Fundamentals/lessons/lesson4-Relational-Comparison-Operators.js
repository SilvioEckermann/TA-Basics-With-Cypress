// relational and comparison operators

// > - greater than
// < - less than
// >= - greater than or equal to
// <= - less than or equal to

var result = 5 > 3;
console.log(result);

// == - equal to
// != - not equal to
// === - equal to and same type
// !== - not equal to and same type

var x = 1;
console.log(x == "1"); // loose comparison
console.log(x === "1"); // strict comparison
console.log(x === 1);
